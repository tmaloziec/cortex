# ─── SESSION STORAGE ──────────────────────────────────────────────────────────
SESSIONS_DIR = Path.home() / ".cortex" / "sessions"
SESSIONS_DIR.mkdir(parents=True, exist_ok=True)

import re as _re
_SAFE_SESSION_ID = _re.compile(r"^[A-Za-z0-9_\-]{1,64}$")

def _valid_session_id(session_id: str) -> bool:
    """Validate session id format and length."""
    return bool(session_id and _SAFE_SESSION_ID.match(session_id))

def _save_session_local(session_id: str, messages: list, meta: dict = None):
    """Save session to local JSON file (atomic write, per-session lock)."""
    if not _valid_session_id(session_id):
        return
    data = {
        "id": session_id,
        "messages": [m for m in messages if m.get("role") != "system"],
        "model": OLLAMA_MODEL,
        "updated_at": datetime.datetime.now().isoformat(),
        "meta": meta or {}
    }
    # title from first user message
    for m in messages:
        if m.get("role") == "user":
            data["title"] = m["content"][:80]
            break

    path = SESSIONS_DIR / f"{session_id}.json"
    serialized = json.dumps(data, ensure_ascii=False, indent=2)

    with _session_lock(session_id):
        # Atomic write: tmp file in same dir + os.replace
        fd, tmp_path = tempfile.mkstemp(
            dir=str(SESSIONS_DIR),
            prefix=f".{session_id}.",
            suffix=".tmp"
        )
        try:
            with os.fdopen(fd, "w", encoding="utf-8") as f:
                f.write(serialized)
            os.replace(tmp_path, path)
        except Exception:
            try:
                os.unlink(tmp_path)
            except OSError:
                pass
            raise

def _load_session_local(session_id: str) -> dict:
    """Load session from file. Returns None on invalid id, missing file, or corrupted JSON."""
    if not _valid_session_id(session_id):
        return None
    path = SESSIONS_DIR / f"{session_id}.json"
    if not path.exists():
        return None
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return None

def _list_sessions_local(limit: int = 30) -> list:
    """List recent sessions sorted by date, skipping corrupted files."""
    sessions = []
    for f in SESSIONS_DIR.glob("*.json"):
        try:
            data = json.loads(f.read_text(encoding="utf-8"))
            sessions.append({
                "id": data.get("id", f.stem),
                "title": data.get("title", "Untitled"),
                "model": data.get("model", "?"),
                "updated_at": data.get("updated_at", ""),
                "msg_count": len(data.get("messages", [])),
            })
        except (json.JSONDecodeError, OSError):
            continue
    sessions.sort(key=lambda x: x["updated_at"], reverse=True)
    return sessions[:limit]

