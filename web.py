# Locks per-session-id for atomic writes
_SESSION_LOCKS: dict = {}
_SESSION_LOCKS_LOCK = threading.Lock()

def _session_lock(session_id: str) -> threading.Lock:
    with _SESSION_LOCKS_LOCK:
        if session_id not in _SESSION_LOCKS:
            _SESSION_LOCKS[session_id] = threading.Lock()
        return _SESSION_LOCKS[session_id]

def _check_auth(token: str) -> bool:
    """Constant-time token comparison. Empty AUTH_TOKEN disables auth."""
    if not AUTH_TOKEN:
        return True
    if not token:
        return False
    return secrets.compare_digest(token, AUTH_TOKEN)

def _require_auth(authorization: str = None, x_token: str = None, query_token: str = None):
    """Extract token from Authorization header, X-Token header, or ?token= query."""
    token = ""
    if authorization and authorization.startswith("Bearer "):
        token = authorization[7:]
    elif x_token:
        token = x_token
    elif query_token:
        token = query_token
    if not _check_auth(token):
        raise HTTPException(status_code=401, detail="Invalid or missing token")

